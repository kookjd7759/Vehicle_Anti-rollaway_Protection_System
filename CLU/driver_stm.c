#include "IfxStm.h"

/*********************************************************************************************************************/
/*-------------------------------------------------Data Structures---------------------------------------------------*/
/*********************************************************************************************************************/

/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
/*********************************************************************************************************************/

volatile scheduling_flag g_scheduling_info;

/*********************************************************************************************************************/
/*--------------------------------------------Private Variables/Constants--------------------------------------------*/
/*********************************************************************************************************************/

static app_stm   stm;
static uint32    counter_1ms = 0u;

/*********************************************************************************************************************/
/*------------------------------------------------Function Prototypes------------------------------------------------*/
/*********************************************************************************************************************/

/* 인터럽트 핸들러 명칭 또한 컨벤션 적용 */
IFX_INTERRUPT(stm_int0_handler, 0, 100);

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/

void driver_stm_init(void)
{
    boolean interrupt_state = IfxCpu_disableInterrupts();

    stm.stm_sfr = &MODULE_STM0;
    IfxStm_initCompareConfig(&stm.stm_config);

    stm.stm_config.triggerPriority = 100u;
    stm.stm_config.typeOfService = IfxSrc_Tos_cpu0;
    stm.stm_config.ticks = 100000u;

    IfxStm_initCompare(stm.stm_sfr, &stm.stm_config);

    IfxCpu_restoreInterrupts(interrupt_state);
}

void stm_int0_handler(void)
{
    IfxCpu_enableInterrupts();

    IfxStm_clearCompareFlag(stm.stm_sfr, stm.stm_config.comparator);
    IfxStm_increaseCompare(stm.stm_sfr, stm.stm_config.comparator, 100000u);

    counter_1ms++;



    g_scheduling_info.scheduling_1ms_flag = 1u;


    if ((counter_1ms % 10u) == 0u)
    {
        g_scheduling_info.scheduling_10ms_flag = 1u;
    }

    if ((counter_1ms % 100u) == 0u)
    {
        g_scheduling_info.scheduling_100ms_flag = 1u;
    }

    if ((counter_1ms % 1000u) == 0u)
    {
        g_scheduling_info.scheduling_1000ms_flag = 1u;
    }
}
